function [y] = p002(x)
%% 计算提成
y = 0;
if x>=100
    y = y + (x-100)*0.01;
    x = 100;
    %% 再衍生下，可以直接预计算100的利润，相加直接返回
    %% 也就是类似个税速算额
end   
if x>=60
    y = y + (x-60)*0.015;
    x = 60;
end
if x>=40
    y = y + (x-40)*0.03;
    x = 40;  
end
if x>=20
    y = y + (x-20)*0.05;
    x = 20;  
end
if x>=10
    y = y + (x-10)*0.075;
    x = 10;  
end
y = y + x*0.1;


